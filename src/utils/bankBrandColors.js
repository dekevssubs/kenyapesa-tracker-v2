/**
 * Get bank/institution color based on institution name and category
 * @param {string} institutionName - The institution name (e.g., "Equity Bank", "Safaricom")
 * @param {string} category - The account category (e.g., "mpesa", "bank")
 * @param {string} accountType - The account type ("cash", "investment", "virtual")
 * @returns {object} Color object with primary, secondary, gradient, and name
 */
export function getBankColor(institutionName, category, accountType = 'cash') {
  // First, try to match by institution name
  if (institutionName) {
    const normalizedName = institutionName.toLowerCase().trim()

    // Direct match
    if (BANK_COLORS[normalizedName]) {
      return BANK_COLORS[normalizedName]
    }

    // Partial match - check if any bank name is contained in the institution name
    for (const [key, value] of Object.entries(BANK_COLORS)) {
      if (normalizedName.includes(key) || key.includes(normalizedName)) {
        return value
      }
    }
  }

  // Second, try to match by category
  if (category && CATEGORY_COLORS[category]) {
    return CATEGORY_COLORS[category]
  }

  // Fallback to account type
  return FALLBACK_COLORS[accountType] || FALLBACK_COLORS.default
}

